def main() -> int:
    root = pathlib.Path(__file__).resolve().parents[1]
    target = root / "app" / "lib" / "kanji-low-grades.json"
    items = json.loads(target.read_text(encoding="utf-8"))

    placeholder_suffix = "を かきます。"
    placeholder_prefix = "かん字ノートに **"

    updated = 0
    missing: list[str] = []
    for item in items:
        ch = item["char"]
        ex = item.get("example", "")
        is_placeholder = ex.startswith(placeholder_prefix) and ex.endswith(
            placeholder_suffix
        )
        if not is_placeholder:
            continue
        if ch in EXAMPLES:
            item["example"] = EXAMPLES[ch]
            updated += 1
        else:
            missing.append(ch)

    target.write_text(
        json.dumps(items, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    print(f"updated: {updated}")
    if missing:
        print(f"missing examples for: {''.join(missing)}")
        return 1
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
