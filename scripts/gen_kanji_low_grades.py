def example_for(char: str, kun: str, _on: str) -> str:
    if char in _EXAMPLE_OVERRIDES:
        return _EXAMPLE_OVERRIDES[char]
    k = _first_kun_token(kun)
    # UI で **…** を赤表示（JSON 編集時も同形式で統一）
    return f"「{k}」とよんで、**{char}**とかきます。"


